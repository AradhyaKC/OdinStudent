const BookInstance = require("../models/bookinstance");

// Display list of all BookInstances.
exports.bookinstance_list = async(req, res,next) => {
  await BookInstance.find({}, '_id book imprint status due_back').populate('book').exec(function(err, result){
    if(err) next(err);
    res.render('bookinstance_list',{title:'book copies existing', bookinstance_list:result});
  });
};

